package north.utils;

public class stata {
    private int re;
    private int root;

    public stata(int re, int root) {
        this.re = re;
        this.root = root;
    }

    public stata() {
    }

    public int getRe() {
        return re;
    }

    public void setRe(int re) {
        this.re = re;
    }

    public int getRoot() {
        return root;
    }

    public void setRoot(int root) {
        this.root = root;
    }
}
